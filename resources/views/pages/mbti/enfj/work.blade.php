                </li>
                <li>
                    مشاغلی را ترجیح می دهند که ایده آلهای آنان را منعکس کند و به دیگران الهام و هماهنگی ببخشد.
                </li>
                <li>
                    دوست دارند اقدامات سریع انجام بدهند.
