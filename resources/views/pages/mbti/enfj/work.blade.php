                </li>
                <li>
                    دوست دارند با اشخاص خلاق و پرانرژی کار کنند و درگیر کارهای متنوع شوند.
                </li>
                <li>
                    از سیاستها و رویه ها تا جایی که با ارزشها و نیازهای آنان منافات نداشته باشد، استفاده می کنند.
                </li>
                <li>
